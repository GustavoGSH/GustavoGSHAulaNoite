let suaNacionalidade = 'Brasil'

switch (suaNacionalidade) {
  case "Brasil":
    console.log("Você é brasileiro!");
    break;
  case "Estados unidos":
    console.log("Você é americano!");
    break;
  case "Japão":
    console.log("Você é japonês!");
    break;
  case "Canadá":
    console.log("Você é canadense!");
    break;
  case "Espanha":
    console.log("Você é espanhol!");
    break;
  case "China":
    console.log("Você é chinês");
    break;
  default:
    console.log("Sua nacionalida não esta cadastrada no site por enquanto!");
    break;
}
